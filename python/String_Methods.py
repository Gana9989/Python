
#Python string Manipulation Examples

#Define the original string
s = "Hello, world!"

#Convert the string to upper case
print(s.upper()) #Out put: 'HELLO, WORLD!'

#Convert the string to lower case
print(s.lower()) #OUT PUT: 'hello, world!'

#Remove leading and trailing white spaces from the string
print(s.strip()) #output : 'Hello, world!'

#Replace all occurrences of 'o' with 'x' in the string
print(s.replace('o','x')) #out put: 'Hellx, wxrld'

#count the nuber of occurences of 'a' in the string
print('Abracadabra'.count('a')) # output: 4